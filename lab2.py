# Введення трьох чисел
a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))
c = int(input("Введіть третє число: "))

# Знаходження двох найменших
min1 = min(a, b, c)

# Видаляємо найменше і знаходимо наступне мінімальне
if min1 == a:
    min2 = min(b, c)
elif min1 == b:
    min2 = min(a, c)
else:
    min2 = min(a, b)

# Обчислення добутку
product = min1 * min2

# Вивід результату
print("Добуток двох найменших чисел:", product)